package weekday1.day1;

public class LearnPrimitiveDataTypes {

	public static void main(String[] args) {

		// Declaring a Character
		
		char a = '$';
		System.out.println(a);
		
		// Declaring a boolean
		
		boolean flag  = true;
		System.out.println(flag);
		
		// Declaring a integer
		
		int b = 212334566;
		System.out.println(b);
		
		//Declaring a byte
		
		byte c = 34;
		System.out.println(c);
		
		//Declaring a Long
		
		long d = 701030974567881L;
		
		//Declaring a short
		
		short e =4000;
		System.out.println(e);
		
		//Declaring a Float
		
		float f =1223.3122f;
		System.out.println(f);
		
		//Declaring a double
		
		double g = 1234.5657232443457;
		System.out.println(g);
		
		String name = "Naveen";
		System.out.println(name);


	}

}
